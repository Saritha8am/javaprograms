package day9;

public class Add {
	
	public void sum(int x,int y)
	{
		System.out.println("Sum is "+(x+y));
	}
	
	public void sum(int x,int y,int z)
	{
		System.out.println("Sum is "+(x+y+z));
	}

	public void sum(double x,double y)
	{
		System.out.println("Sum is "+(x+y));
	}
	
	
	public void sum(long x,long y)
	{
		System.out.println("Sum is "+(x+y));
	}
	
	
}
